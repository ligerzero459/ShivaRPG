function AIRemote.DestroyTemplateHud ( )
--------------------------------------------------------------------------------
	
	local hRoot = hud.getComponent ( this.getUser ( ), "AIRemote.Root" )
    if ( hRoot )
    then
        hud.destroyComponent ( hRoot )
    end
	
--------------------------------------------------------------------------------
end
