function AIRemote.connected_onLoop ( )
--------------------------------------------------------------------------------
	
    local hCurrentServer = network.getCurrentServer ( )
    if ( ( hCurrentServer == nil ) or ( server.getStatus ( hCurrentServer ) == server.kStatusNone ) )
    then
        network.disconnect ( )
        this.idle ( )
    end
	
--------------------------------------------------------------------------------
end
